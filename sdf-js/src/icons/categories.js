export const CATEGORY_NAMES = Object.keys(CATEGORIES);

// Legacy aliases kept for callers that import the old function names
export function getAllIconNames() {
  return Array.from(new Set(Object.values(CATEGORIES).flat()));
}

export function getIconNamesForCategory(category) {
  return CATEGORIES[category] ?? [];
}

/**
 * Get curated icon names for a category. Returns [] for unknown category.
 * @param {string} category
 * @returns {string[]}
 */
export function getCategoryNames(category) {
  return CATEGORIES[category] ?? [];
}

/**
 * Find which category an icon name belongs to. Returns first match or null.
 * @param {string} name
 * @returns {string|null}
 */
export function getCategoryForIcon(name) {
  for (const [cat, names] of Object.entries(CATEGORIES)) {
    if (names.includes(name)) return cat;
  }
  return null;
}
